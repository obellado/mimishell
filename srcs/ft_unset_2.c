#include "../minishell.h"

int	ft_check_unset(char *n, char **en)
{
	int		i;
	int		j;

	i = 1;
	if (n[0] < 65 || (n[0] > 90 && n[0] < 95) || n[0] == 96 || n[0] > 122)
		return (-1);
	while (n[i] != '\0')
	{
		if (n[i] < 48 || (n[i] > 57 && n[i] < 65) || (n[i] > 90 && \
		n[i] < 95) || n[i] == 96 || n[i] > 122)
			return (-1);
		i++;
	}
	j = 0;
	while (en[j] != NULL)
	{
		if ((ft_strncmp(n, en[j], i)) == 0)
		{
			if (en[j][i] == '=')
				return (j);
		}
		j++;
	}
	return (-2);
}
